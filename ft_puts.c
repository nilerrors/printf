#include "ft_printf.h"

static size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

size_t	ft_puts(const char *s)
{
	if (!s)
		return (0);
	return (write(1, s, ft_strlen(s)));
}
